// &&(and)/||(or) conditions

// and/or used to compare two statements together.
// in "and" both the statement should be true.
// For eg:
let name = "veer";
let age = 22;

if (name[0] === "v" && age >= 18) {
    console.log("You are eligible");
} else {
    console.log("You are not eligible");
}

// or ||
// in "or" either one statement should be true.
// for eg:

if (name[0] === "k" || age > 18) {
    console.log("You are eligible");
} else {
    console.log("You are not eligible");
}




