// == vs ===

// ""=="" eg:
let k = "7";
let j = 7;

console.log(k == j);      // true
// ==(double equalto) only check if the value is same then true, it doesn't check the data type. Now if the k value is 8 then it will show false.

// "===" eg:
let z = 99;
let m = "99";

console.log(z === m);       // false
// ===(triple equalto) check both data type and value.

console.log("*****************************");

// != vs !== 

// != eg: 

console.log(z != m);         // false
// !=(not equalto) doesn't check the data type it only see the value. Now if m value is 100 then it will print true.

console.log(z !== m);       // true
// !=(not equalto) this will check both data type and value. if the value and datatype are similar then it will show false.

