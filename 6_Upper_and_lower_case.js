// toUppercase()

// To capitalize the string
let firstName = "Veer";
console.log(firstName);

console.log(firstName.toUpperCase());
// this will give the new string not modify the original string.

firstName = firstName.toUpperCase();
console.log(firstName);
// this will change the original string to uppercase.

console.log("************************");

// toLowercase
// same format of uppercase

let lastName = "SHINDE";
console.log(lastName);
console.log(lastName.toLowerCase());
// this will give the new string not modify the original string.

lastName = lastName.toLowerCase();
console.log(lastName);
// this will change the original string to lowercase.








